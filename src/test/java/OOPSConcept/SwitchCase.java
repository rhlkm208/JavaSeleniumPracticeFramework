package OOPSConcept;

public class SwitchCase {

	public static void main(String[] args) {

		int a = 5;

		switch (a) {
		case 0:
			System.out.println("Hello");
		//	break;
		case 1:
			System.out.println("Hi");
		//	break;
		case 2:
			System.out.println("Hey");
		//	break;
		case 3:
			System.out.println("Welcome");
		//	break;
		default:
			System.out.println("Default case");
			break;
		}

	}

}
